package com.java;

public class pattern8 {
	    public static void main(String[] args) {
	        int rows = 5; // You can change the number of rows here

	        for (int i = rows; i >= 1; i--) {
	            for (int j = 1; j <= i; j++) {
	                System.out.print(j);
	            }
	            System.out.println();
	        }
	    }
	
}
